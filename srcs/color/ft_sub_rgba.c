#include "color.h"

t_rgba	ft_sub_rgba(t_rgba c1, t_rgba c2)
{
	c1.r -= c2.a * c2.r;
	c1.g -= c2.a * c2.g;
	c1.b -= c2.a * c2.b;
	c1 = ft_clamp_rgba(c1);
	return (c1);
}
